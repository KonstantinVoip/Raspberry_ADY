   /*  CPU C8051F902  SYSCLK = 24.5 [Мгц] = 24 500 000 [Гц]  TIMER_16_bit = 0xFFFF = 65535
    *  
    *  -----------------------------------------------------------------------------------------------------
    *  Временной предел таймера.
	*  0x0001 = 1 тик      [процессорных_циклов]  частота_sysclk = 24.5 [Мгц] = 24 500 000 [Гц]
	*  время 1 прерывания = 4.081632653 * 10 -8 [c]  = 0.041 [мКс]
	*  0xFFFF = 65535 тиков[процессорных_циклов]  частота_hclk = 24.5 [Мгц]   = 24 500 000 [Гц]
    *  время 1 прерывания = 0.00267489 [c] = 2.67489 [мс] //24.5 [Мгц]
    *                       
    * 
    *  //Устанаваливаем время прерывания 1[мс] и 1 [мкс]
    *  Для того чтобы у нас считали одинаково таймеры для DSP и RISC количество процессорных циклов = 0x5FB4 = 24500
    *  частота_hclk = 24.5 [Мгц] = 24 500 000 [Гц]
    *
    *  время 1 прерывания = 0.001 [c] =    1 [мс] //24.5 [Мгц]
    *  
	*
	*
	*  Для того чтобы у нас считали одинаково таймеры для DSP и RISC количество процессорных циклов = 0x0018 = 24
    *  время 1 прерывания = 0.000001 [c] = 1 [мКс] //24 [Мгц]
    *
    *  задержки(delay):
    *
    *
    *  приблизительная формула для подсчёта: время[с] = [Процессорные циклы] /[частоту]
    *  максимум =  16 777 215 тиков   = 0.699 [секунд]  при 24 [Мгц]
    *
    *  приблизительная формула для подсчёта: время[с] = [Процессорные циклы] /[частоту]
    *  максимум =  65535 тиков   = 0.002730625 [секунд]  при 24 [Мгц]
    *              24000 тиков   = 0.001 [секунд]  =1 [мс] при 24 [Мгц]
    */

